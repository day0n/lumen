export const messages = { en, zh };
export type MessageKey = string;

export function translate(locale: Locale, key: MessageKey, params?: Params): string {
  const template = readMessage(messages[locale], key) ?? readMessage(messages.en, key) ?? key;
  return interpolate(template, params);
}

export function readMessage(source: unknown, key: string): string | undefined {
  const value = key.split('.').reduce<unknown>((current, segment) => {
    if (!current || typeof current !== 'object') return undefined;
    return (current as Record<string, unknown>)[segment];
  }, source);
  return typeof value === 'string' ? value : undefined;
}

export function readMessageArray(locale: Locale, key: string): string[] {
  const value = key.split('.').reduce<unknown>((current, segment) => {
    if (!current || typeof current !== 'object') return undefined;
    return (current as Record<string, unknown>)[segment];
  }, messages[locale]);
  return Array.isArray(value)
    ? value.filter((item): item is string => typeof item === 'string')
    : [];
}

export function readMessageObjectArray<T extends Record<string, unknown>>(
  locale: Locale,
  key: string,
): T[] {
  const value = key.split('.').reduce<unknown>((current, segment) => {
    if (!current || typeof current !== 'object') return undefined;
    return (current as Record<string, unknown>)[segment];
  }, messages[locale]);
  return Array.isArray(value)
    ? value.filter(
        (item): item is T => typeof item === 'object' && item !== null && !Array.isArray(item),
      )
    : [];
}

function interpolate(template: string, params?: Params): string {
  if (!params) return template;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key) => {
    const value = params[key];
    return value === undefined || value === null ? match : String(value);
  });
}
